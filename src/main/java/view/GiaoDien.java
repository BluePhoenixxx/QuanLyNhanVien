    private void Table_NVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_NVMouseClicked
       int SelectIndex = Table_NV.getSelectedRow();
        List<NhanVien> ls = new ArrayList<>();
        ls =nvdao.getAll();
        NhanVien nv =ls.get(SelectIndex);
        Text_MaNV.setText(nv.getMaNV());
        Text_TenNV.setText(nv.getTenNV());
        TextEmail.setText(nv.getEmail());
        TextSDT.setText(nv.getSDT());
        text_Date.setText(nv.getNgaySinh());
        if (nv.getGioiTinh().equals("Nam"))
            Btn_Nam.setSelected(true);
        else
            Btn_Nu.setSelected(true);
        TextDiaChi.setText(nv.getDiaChi());
        int  n = CB_CV.getItemCount();
        DefaultComboBoxModel cbModel = (DefaultComboBoxModel) CB_CV.getModel();
        cbModel.setSelectedItem(nv.getTenCV());

    }//GEN-LAST:event_Table_NVMouseClicked

