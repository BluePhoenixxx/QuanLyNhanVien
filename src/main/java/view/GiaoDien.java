    public void getCBNhanVien(){
        DefaultTableModel cbModel = (DefaultTableModel) Table_NV.getModel();
        cbModel.setRowCount(0);
        for (NhanVien nv : nvdao.getAll()) {
            Vector rowData = new Vector();
            rowData.add(cbModel.getRowCount()+1);
            rowData.add(nv.getMaNV());
            rowData.add(nv.getTenNV());
            rowData.add(nv.getTenCV());
            rowData.add(nv.getEmail());
            rowData.add(nv.getSDT());
            rowData.add(nv.getDiaChi());
            rowData.add(nv.getGioiTinh());
            rowData.add(nv.getNgaySinh());
            cbModel.addRow(rowData);
        }
        
    }
